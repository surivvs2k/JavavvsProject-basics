
public class Operators {

	public static void main(String[] args) 
	
	{
		
		//Arithmetic Operators
		
		System.out.println("Output for Arithmetic Operators");

		
		int a = 10;
		int b = 20;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		//Relational Operators
		
		System.out.println("Output for Relational Operators");

		
		System.out.println(a<b);	//true
		System.out.println(a>b);	//false
		System.out.println(a<=b);	//true
		System.out.println(a>=b);	//false
		System.out.println(a==b);	//false
		System.out.println(a!=b);	//true
		
		
		//Logical Operators
		
		System.out.println("Output for Logical Operators");
		
		boolean x = true;
		boolean y = false;
		
		System.out.println(x && y);
		System.out.println(x || y);
		System.out.println(!x);
		System.out.println(!y);
		
		
		
		
		int c;
		c=a;
		System.out.println(c); // output for c 10 (a value)
		
		c=b;
		System.out.println(c); // output for c 20 (b value)

		c = 100;
		
		//c++; // c=c+1;  // Increment Operator // Output is 101
		
		//c--; // c=c-1; // Decrement Operator // Output is 99
		
		//c+=5; // c=c+5;  // Increment Operator // Output is 105
		
		c-=5; // c=c-5; // Decrement Operator // Output is 95
		
		
		System.out.println(c);
		
		






	}

}
